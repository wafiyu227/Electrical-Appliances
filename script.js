// Add click event listeners for each navigation item
document.getElementById('home').addEventListener('click', () => {
    window.location.href = 'index.html'; // Redirect to Home page
});

document.getElementById('about').addEventListener('click', () => {
    window.location.href = 'about.html'; // Redirect to About page
});

document.getElementById('services').addEventListener('click', () => {
    window.location.href = 'products.html'; // Redirect to Services page
});

document.getElementById('contact').addEventListener('click', () => {
    window.location.href = 'contact.html'; // Redirect to Contact page
});
